package com.abuilder.parser.toolbox;

public abstract class ScriptObject {
	
	enum ObjectType {
		DEFINITION, RULE, STATE
	}
	
	private final int id;
	private final ABIdentifier identifier;
	protected ObjectType type;
	
	public ScriptObject(int id, ABIdentifier identifier, ObjectType type) {
		this.id = id;
		this.identifier = identifier;
		this.type = type;
	}

	public int getID() {
		return id;
	}

	public ABIdentifier getIdentifier() {
		return identifier;
	}

	public ObjectType getType() {
		return type;
	}

	protected void setType(ObjectType type) {
		this.type = type;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + id;
		result = prime * result + ((identifier == null) ? 0 : identifier.hashCode());
		result = prime * result + ((type == null) ? 0 : type.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ScriptObject other = (ScriptObject) obj;
		if (id != other.id)
			return false;
		if (identifier == null) {
			if (other.identifier != null)
				return false;
		} else if (!identifier.equals(other.identifier))
			return false;
		if (type != other.type)
			return false;
		return true;
	}

	public String getName() {
		return identifier.getName();
	}
}
